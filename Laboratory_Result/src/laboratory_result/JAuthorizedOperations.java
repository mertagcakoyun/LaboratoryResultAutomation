    private void AddAnalysisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddAnalysisButtonActionPerformed

        if (PatientIdText.getText().isEmpty() || PatientNameText.getText().isEmpty() || PatientSurnameText.getText().isEmpty() || AnalysisIdText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fiil in the blanks");
        } else {
            try {
                
            
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Laboratory_ResultPU");
            EntityManager em = emf.createEntityManager();

            Patient patient = new Patient();
            patient.setId(Integer.parseInt(PatientIdText.getText()));
            patient.setName(PatientNameText.getText());
            patient.setSurname(PatientSurnameText.getText());
            patient.setContact(PatientContactText.getText());
            patient.setGender((String) GenderCombobox.getSelectedItem());
            em.getTransaction().begin();
            em.persist(patient);
            em.getTransaction().commit();
            em.close();
            emf.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, " This id is already used. Please continue to add new analysis ...");
            }
            if (AnalysisTypeComboBox.getSelectedItem().equals("Blood Analysis")) {
                jPanel1.setVisible(false);
                PanelBlood.setVisible(true);

            } else if (AnalysisTypeComboBox.getSelectedItem().equals("Pathology Analysis")) {
                jPanel1.setVisible(false);
                PanelPathology.setVisible(true);

            } else if (AnalysisTypeComboBox.getSelectedItem().equals("Urine Analysis")) {
                jPanel1.setVisible(false);
                PanelUrine.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Please select analysis...");
            }
            clickBCounter = 0;
            clickPCounter = 0;
            clickUCounter = 0;
        }
    }//GEN-LAST:event_AddAnalysisButtonActionPerformed

