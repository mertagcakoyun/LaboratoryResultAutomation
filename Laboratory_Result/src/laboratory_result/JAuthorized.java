    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        JAuthorizedOperations authorizedOperations = new JAuthorizedOperations();
         String password="";
         String username="";
        try {
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/LaboratoryDB", "lab", "lab");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM AUTHORIZED");
            while (rs.next()) {
                password = rs.getString("PASSWORD");
                username = rs.getString("USERNAME");
                System.out.println(username+" "+password);

            }

            if (username.equals(usernameTf.getText()) && password.equals(passwordTf.getText())) {
                authorizedOperations.setVisible(true);
                dispose();
            } else {
                ErrorLabel.setVisible(true);

            }

        } catch (SQLException ex) {
            Logger.getLogger(JAuthorized.class.getName()).log(Level.SEVERE, null, ex);
            
        }


    }//GEN-LAST:event_loginBtnActionPerformed

